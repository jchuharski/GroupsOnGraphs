function random_search(adjacent::Matrix{Float64})
    n = Int64(sqrt(length(adjacent)))
    start = rand((1:n))
    start_row = adjacent[start, :]
    seen = [start]
    cur = start
    count = 0
    for i = 1:n-1
        count +=1
        minsum = Inf
        min = 0
        for id in 1:n
            if adjacent[cur, id] == 1
                s = Int64(sum(adjacent[:, id]))
                if s > 0 && s < minsum
                    minsum = s
                    min = id
                end
            end
        end
        for i in 1:n
            adjacent[cur, i] = 0 
            adjacent[i, cur] = 0
        end
        if minsum == Inf
            return false
        end
        cur = min
        push!(seen, cur)
    end
    if start_row[cur] == 1
        push!(seen, start)
        return seen
    end
    return false
end

